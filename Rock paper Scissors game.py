import random
while True:
    Choices = ("rock","paper","scissors")
    choice=random.choice(Choices)
    U_inp = input("Make a choice:")
    U_inp = U_inp.lower()
    if U_inp == Choices[0]:
        if choice == Choices[1]:
            print(f"AI chooses {choice}")
            print("AI wins")
        else:
            print(f"AI chooses {choice}")
            print("Human wins")
            
        
    elif U_inp == Choices[1]:
        if choice == Choices[2]:
            print(f"AI chooses {choice}")
            print("AI wins")
        else:
            print(f"AI chooses {choice}")
            print("Human wins")
            
    elif U_inp == Choices[2]:
        if choice == Choices[0]:
            print(f"AI choose {choice}")
            print("AI wins")
        else:
            print(f"AI chooses {choice}")
            print("Human wins")
    elif U_inp == choice:
        print('You draw')
            
    play_again=input("Do you want try again:")
    if play_again!="y":
        print("You quit")
        break
    
